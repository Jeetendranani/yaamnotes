"""
Glossary

    method:
    A function that is associated with an object and called using dot notatioin.

    loop:
    A part of program that can run repeatedly.

    encapsulation:
    The process of transforming a sequence fo statements into a function definition.

    generalization:
    The process of replacing something unnecessarily specific (like a number) with something appropriately general (like
    a variable or parameter).

    keyword argument:
    An argument that includes the name of the parameter as a "keyword".

    interface:
    A description of how to use a function, including the name and descriptions of the arguments and the return value.

    refactoring:
    The process of modifying a working program to improve function interfaces and other qualities of the code.

    development plan:
    A process for writing programs.

    docstring:
    A string that appears at the top of function definition to document the function's interface.

    precondition:
    A requirement that should be satisfied by the caller before a function starts.

    postcondition:
    A requirement that should be satisfied by the function before its ends.
"""